package exam;

public class 친구 {

	public static void main(String[] args) {
		// c+s+f : 자동포맷팅(코딩정리) 문법오류가 있을 때는 해주지 않는다.
		
		// 패키지 : exam
		// 클래스 : 친구
		
		// 키(정수를 변수에 저장)
		int 키 = 180;
		// 몸무게(실수)를 변수에 저장
		double 몸무게 = 70;
		// 성별을 변수에 저장
		char 성별 = '남';
		//이름을 변수에 저장
		String 이름 = "김찬우";
		
		// 위 모든 내용을 친절하게 출력
		
		System.out.println("내 친구의 키는 " + 키 + " 입니다. ");
		System.out.println("내 친구의 몸무게는 " + 몸무게 + " 입니다. ");
		System.out.println("내 친구의 성별은 " + 성별 + " 입니다. ");
		System.out.println("내 친구의 이름은 " + 이름 + " 입니다. ");

	}
}
