package 데이터;

public class 기본데이터2 {

	public static void main(String[] args) {
		// 기본 데이터 저장
		// 정수, 실수 저장
		
		int x = 250; //-21억~21까지 컴퓨터는 정수로 인지한다 정수의 크기에 따라서 잡을수 있는 키워드가 4개정도 있다
		double y = 5.5; //실수를 쓸때는 double로 쓴다 , 정수는 int로 쓴다
		char z = '한'; //한글자 문자인 경우 ' 사용  문자를 쓸때는 char 라고 쓴다
		boolean p = true; // false 
		// 보라색은 변경할 수 없다.
		
		
		System.out.println("x:" + x); //""설명해줄때 , + 뒤이어서
		System.out.println("y:" + y); // 컴퓨터에 나오는 곳에 나오게 해줘 라인을 바꿔서
		System.out.println("z:" + z); // 위에 써주고 아래 복붙 
		System.out.println("p:" + p);

		

	}

}
