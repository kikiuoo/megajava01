package 데이터;

public class 나 {

	public static void main(String[] args) {
		// 기본 데이터 연습
		// 저장공간을 변수라고 부른다.
		// 저장공간이름을 변수명이라고 부른다.
		int 나이 = 100; // 정수  // 저장데이터 뭐저장할지 써주기-> 이름-> = -> 크기정해주기 -> ;
		double 시력 = 0.8; // 실수
		char 성별 = '여'; // 문자 char에는 무조건 한글자만 쓸 수 있다. 한글 영어 모두
		boolean 저녁 = true; // 논리
		
		//실행(run)단축키 : 컨트롤 + f11
		// 자동완성방법 : 단축키
		// syso, sysout이라고 치고 + 컨트롤 + 스페이스바
		System.out.println("내 나이는 " + 나이 + "세");
		System.out.println("내 시력은 " + 시력 +"이야");
		System.out.println("내 성별은 " + 성별 + "자야");
		System.out.println("내 저녁은 " + 저녁);
	}
	 

}
